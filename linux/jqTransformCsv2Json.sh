jq -Rsn '
  { "jobs":
   [inputs
   | . / "\n"
   | (.[] | select(length > 0) | . / ",") as $input
   | { "framesize": $input[0],
       "numframes": $input[1],
       "fps": $input[2],
       "zsize": $input[3],
       "numhosts": $input[4],
       "numframes": $input[5],
       "streams": $input[6],
       "codecname": $input[7]
     }
     ]
   }
   ' < jobs.csv

